let display = document.querySelector('.value');

function showthevalue(value) {
    display.value += value;
}

function clearall() {
    display.value = '';
}

function backspace() {
    display.value = display.value.slice(0, -1);
}

function calculate() {
    try {
        display.value = eval(display.value);
    } catch (error) {
        display.value = 'Error';
    }
}
